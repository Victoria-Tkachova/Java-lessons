public class Datatypes {
    public static void main(String[] args) {
        {
            byte v = 125;
            System.out.println(v);
        }
        {
            short v = 31456;
            System.out.println(v);
        }
        {
            int v = 13467889;
            System.out.println(v);
        }
        {
            long v=1234567892;
            System.out.println(v);
        }
        {
            float v=3.145f;
            System.out.println(v);
        }
        {
            double v=345.678;
            System.out.println(v);
        }
        {
            boolean v=true;
            System.out.println(v);
        }
        {
            char v=5678;
            System.out.println(v);
        }
    }
}
